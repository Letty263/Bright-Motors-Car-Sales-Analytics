--- Create cleaned and transformed table (simplified version)
CREATE OR REPLACE TABLE car_sales_final AS
SELECT
    year,
    make,
    model,
    trim,
    body,
    vin,

    -- Handle missing categorical values
    COALESCE(state, 'Unknown') AS state,
    COALESCE(transmission, 'Unknown') AS transmission,
    COALESCE(color, 'Unknown') AS color,
    COALESCE(interior, 'Unknown') AS interior,

    -- Numeric cleaning
    CAST(sellingprice AS DOUBLE) AS sellingprice,
    CAST(mmr AS DOUBLE) AS mmr,

    -- Handle missing odometer
    COALESCE(odometer, 0) AS odometer,

    -- Keep original sale date for now
    saledate,

    -- =====================
    -- FEATURE ENGINEERING
    -- =====================

    -- Total revenue
    CAST(sellingprice AS DOUBLE) AS total_revenue,

    -- Profit margin
    ROUND(
        ((CAST(sellingprice AS DOUBLE) - CAST(mmr AS DOUBLE))
        / CAST(sellingprice AS DOUBLE)) * 100,
        2
    ) AS profit_margin,

    -- Profit tier
    CASE
        WHEN ((CAST(sellingprice AS DOUBLE) - CAST(mmr AS DOUBLE))
             / CAST(sellingprice AS DOUBLE)) * 100 >= 20
            THEN 'High Margin'

        WHEN ((CAST(sellingprice AS DOUBLE) - CAST(mmr AS DOUBLE))
             / CAST(sellingprice AS DOUBLE)) * 100 BETWEEN 10 AND 19
            THEN 'Medium Margin'

        ELSE 'Low Margin'
    END AS profit_tier

FROM `workspace`.`default`.`car_sales_data_clean`

WHERE sellingprice IS NOT NULL
  AND make IS NOT NULL
  AND model IS NOT NULL;

