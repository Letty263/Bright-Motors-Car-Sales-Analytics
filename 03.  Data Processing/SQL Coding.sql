CREATE OR REPLACE TABLE car_sales_analytics AS


SELECT
    year,
    make,
    model,
    trim,
    body,
    transmission,
    vin,
    COALESCE(state, 'Unknown') AS state,
    COALESCE(color, 'Unknown') AS color,
    COALESCE(interior, 'Unknown') AS interior,

    -- condition is reserved in some SQL engines
    COALESCE(`condition`, 0) AS condition_score,
    COALESCE(odometer, 0) AS odometer,
    CAST(sellingprice AS DOUBLE) AS sellingprice,
    CAST(mmr AS DOUBLE) AS mmr,
    saledate,
  
    CASE
        WHEN `condition` >= 40 THEN 'Excellent'
        WHEN `condition` BETWEEN 30 AND 39 THEN 'Good'
        WHEN `condition` BETWEEN 20 AND 29 THEN 'Average'
        ELSE 'Poor'
    END AS condition_bracket,
    CASE
        WHEN odometer < 50000 THEN 'Low Mileage'
        WHEN odometer BETWEEN 50000 AND 100000 THEN 'Medium Mileage'
        ELSE 'High Mileage'
    END AS mileage_bracket,
    CASE
        WHEN sellingprice < 10000 THEN 'Budget'
        WHEN sellingprice BETWEEN 10000 AND 30000 THEN 'Mid Range'
        ELSE 'Luxury'
    END AS price_bracket,
    CAST(sellingprice AS DOUBLE) AS total_revenue,
    CAST(sellingprice AS DOUBLE) - CAST(mmr AS DOUBLE)
    AS price_difference,
    ROUND(
        (
            (CAST(sellingprice AS DOUBLE) - CAST(mmr AS DOUBLE))
            / CAST(sellingprice AS DOUBLE)
        ) * 100,
        2
    ) AS profit_margin,
    CASE
        WHEN (
            (CAST(sellingprice AS DOUBLE) - CAST(mmr AS DOUBLE))
            / CAST(sellingprice AS DOUBLE)
        ) * 100 >= 20
        THEN 'High Margin'
        WHEN (
            (CAST(sellingprice AS DOUBLE) - CAST(mmr AS DOUBLE))
            / CAST(sellingprice AS DOUBLE)
        ) * 100 BETWEEN 10 AND 19
        THEN 'Medium Margin'
        ELSE 'Low Margin'
    END AS profit_tier
FROM `workspace`.`default`.`car_sales_data_clean`
WHERE sellingprice IS NOT NULL
AND make IS NOT NULL
AND model IS NOT NULL;



SELECT
    saledate,

    YEAR(
        TO_TIMESTAMP(saledate, 'E MMM dd yyyy HH:mm:ss')
    ) AS sale_year,

    MONTH(
        TO_TIMESTAMP(saledate, 'E MMM dd yyyy HH:mm:ss')
    ) AS sale_month,

    DATE_FORMAT(
        TO_TIMESTAMP(saledate, 'E MMM dd yyyy HH:mm:ss'),
        'MMMM'
    ) AS month_name,

    DATE_FORMAT(
        TO_TIMESTAMP(saledate, 'E MMM dd yyyy HH:mm:ss'),
        'EEEE'
    ) AS day_name

FROM car_sales_analytics
LIMIT 10;
